# podemos manipular strings com indeces negativos
a = "Teste de como trabalhar com Strings"
print(a[-5])
print(a[-4])
print(a[-3])
print(a[-2])
print(a[-1])
# Tornar maiusculas
b = a.upper()
print(b)
# Tornar minusculas
c = b.lower()
print(c)
# Pdemos separar seprar as palavras
d = a.split()
print(d)
